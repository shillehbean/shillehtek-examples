# Basic quick-start steps to use the analyzer with Saleae Logic v1 (legacy): install software, plug in the device, enable protocol analyzers on channels, and start a capture.
#
# Buy this module: https://shillehtek.com/products/24mhz-8-channel-usb-logic-analyzer-digital-debugger-for-arduino
# Full manual: https://shillehtek.com/blogs/shillehtek-product-manuals/24mhz-8-channel-usb-logic-analyzer-digital-debugger-for-arduino-manual
# More examples: https://github.com/shillehtek/examples
#

# 1. Download Saleae Logic v1.2.x ("Legacy") from saleae.com/downloads
#    The newer Logic 2 software does NOT support clones - use Logic 1.x.
# 2. Plug in the analyzer via USB.
# 3. Open Logic. It auto-detects "Saleae Logic 8".
# 4. Click the gear next to a channel to enable an analyzer (I2C, SPI, etc.)
# 5. Hit Start to capture.
